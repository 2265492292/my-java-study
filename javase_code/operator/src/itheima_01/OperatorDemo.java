package itheima_01;

/*
算术运算符
*/
public class OperatorDemo {
    public static void main(String[] args) {
        int a=6;
        int b=5;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
        //整数相除只能得到整数；
        System.out.println(6.0/5);
    }
}
